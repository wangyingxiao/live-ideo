//时间戳转换时间  
function formatTime(number) {
  var n = number * 1;
  var date = new Date(n);
  var Y = date.getFullYear() + '/';
  var M = (date.getMonth() + 1 < 10 ? '0' + (date.getMonth() + 1) : date.getMonth() + 1) + '/';
  var D = date.getDate() < 10 ? '0' + date.getDate()+1 : date.getDate()+1;
  return (Y + M + D)
}
module.exports = {
  formatTime: formatTime
}